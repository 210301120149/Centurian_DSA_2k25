package LEC1;
public class Arithmetic {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        int c=a+b;
        System.out.println(c);
        int d=b-a;
        System.out.println(d);
        int e=a*b;
        System.out.println(e);
        int f=a/b;
        System.out.println(f);
        int g=a%b;
        System.out.println(g);
    }
}
